package com.project.shop.oder_product;

public class Order_ProductDTO {
	private int opno;
	private int pno;
	private int ono;
	private String id;
	private int count;
	private int price;
	public int getOpno() {
		return opno;
	}
	public void setOpno(int opno) {
		this.opno = opno;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getOno() {
		return ono;
	}
	public void setOno(int ono) {
		this.ono = ono;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}
