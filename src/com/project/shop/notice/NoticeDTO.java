package com.project.shop.notice;

public class NoticeDTO {
	private int shop_notice_no;
	private String shop_notice_title;
	private String shop_notice_contents;
	private String shop_notice_writer;
	private String shop_notice_date;
	public int getShop_notice_no() {
		return shop_notice_no;
	}
	public void setShop_notice_no(int shop_notice_no) {
		this.shop_notice_no = shop_notice_no;
	}
	public String getShop_notice_title() {
		return shop_notice_title;
	}
	public void setShop_notice_title(String shop_notice_title) {
		this.shop_notice_title = shop_notice_title;
	}
	public String getShop_notice_contents() {
		return shop_notice_contents;
	}
	public void setShop_notice_contents(String shop_notice_contents) {
		this.shop_notice_contents = shop_notice_contents;
	}
	public String getShop_notice_writer() {
		return shop_notice_writer;
	}
	public void setShop_notice_writer(String shop_notice_writer) {
		this.shop_notice_writer = shop_notice_writer;
	}
	public String getShop_notice_date() {
		return shop_notice_date;
	}
	public void setShop_notice_date(String shop_notice_date) {
		this.shop_notice_date = shop_notice_date;
	}
	
}
